INSERT INTO CC_COBRA.FR02_DEF_INT_BW_CONV
SEL    A.ORG_TX
      ,BILLNG_CYCLE_NO
      ,CCA.CREDIT_CARD_LOGO_CD
      ,R.REPORT_DT 
      ,LU.BALANC_TYPE_CD  
	  ,COALESCE(LU2.BALANC_TYPE_CD, 'PURGED') AS NM_BALANC_TYPE_CD 
      ,SUM(AFS.AGRMNT_FEATUR_AM)
      ,COUNT(*) AS VOLUME

FROM   GDW_VIEWS.CREDIT_CARD_AGREEMENT CCA
JOIN   CC_COBRA.CC_AGREEMENT_ADDED_DATE A
ON     CCA.AGRMNT_ID = A.AGRMNT_ID
AND    CCA.SOURCE_END_DT = '3500/12/31'
JOIN   CC_COBRA.WK_FR02_REPORT_DATE_R2 R
ON     A.PURGED_DT IS NULL OR A.PURGED_DT > R.REPORT_DT
JOIN   GDW_VIEWS.AGREEMENT_FEATURE AF
ON     CCA.AGRMNT_ID = AF.AGRMNT_ID	
AND    R.REPORT_DT BETWEEN AF.SOURCE_START_DT AND AF.SOURCE_END_DT - 1
AND    AF.EXPIRY_DT IS NULL
JOIN   GDW_VIEWS.AGREEMENT_FEATURE_SUMMARY AFS
ON     CCA.AGRMNT_ID = AFS.AGRMNT_ID
AND    AF.FEATUR_ID = AFS.FEATUR_ID
AND    AFS.AGRMNT_FEATUR_SUMMRY_TYPE_CD ='00500010'	
AND  ABS(AFS.AGRMNT_FEATUR_AM) <> 9999999999999.99
AND     R.REPORT_DT BETWEEN AFS.SOURCE_START_DT AND AFS.SOURCE_END_DT-1
JOIN   CC_COBRA.FR15_AGRMNT_FEATUR_PLAN_TYPE L
ON     CCA.AGRMNT_ID = L.AGRMNT_ID
AND    AFS.FEATUR_ID = L.FEATUR_ID
AND    R.REPORT_DT BETWEEN L.SOURCE_START_DT AND L.SOURCE_END_DT-1
JOIN   CC_COBRA.FR15_BALANC_TYPE_CD_LOOKUP LU  --CHANGE JM 2017-05-19
ON     L.NEW_BALANC_TYPE_CD = LU.BALANC_TYPE_CD
LEFT JOIN CC_COBRA.FR15_AGRMNT_FEATUR_PLAN_TYPE L2
ON     CCA.AGRMNT_ID = L2.AGRMNT_ID
AND    AFS.FEATUR_ID = L2.FEATUR_ID
AND    ADD_MONTHS(R.REPORT_DT+1,1)-1 BETWEEN L2.SOURCE_START_DT AND L2.SOURCE_END_DT-1
LEFT JOIN   CC_COBRA.FR15_BALANC_TYPE_CD_LOOKUP LU2  --CHANGE JM 2017-05-19
ON     L2.NEW_BALANC_TYPE_CD = LU2.BALANC_TYPE_CD
GROUP BY 1,2,3,4,5,6;

.IF ERRORCODE <> 0 THEN .GOTO FOUNDERROR;

