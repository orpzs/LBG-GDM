INSERT INTO CC_COBRA.FR02_DEF_INT_BW_CONV_REPORT
SELECT A.ORG_TX
      ,A.PERIOD_END_DT
      ,'A' --ACTUAL
      ,A.IBB_DEFERRED_INT_AM
      ,B.IBB_DEFERRED_CONV_INT_AM
      ,CASE WHEN A.IBB_DEFERRED_INT_AM > 0 
             THEN CAST(CAST(B.IBB_DEFERRED_CONV_INT_AM AS FLOAT)/CAST(A.IBB_DEFERRED_INT_AM AS FLOAT) AS DECIMAL(12,9))
            ELSE 0 END AS IBB_DEFERRED_CONV_INT_PC
      ,C.NIBB_DEFERRED_INT_AM  
      ,D.NIBB_DEFERRED_CONV_INT_AM
      ,CASE WHEN C.NIBB_DEFERRED_INT_AM > 0 
             THEN CAST(CAST(D.NIBB_DEFERRED_CONV_INT_AM AS FLOAT)/CAST(C.NIBB_DEFERRED_INT_AM AS FLOAT) AS DECIMAL(12,9))
            ELSE 0 END AS NIBB_DEFERRED_CONV_INT_PC          
      
FROM 
(
SELECT ORG_TX
      ,PERIOD_END_DT
      ,'A' AS REC_TYPE
      ,SUM(DEFERRED_INT_AM) AS IBB_DEFERRED_INT_AM
FROM   CC_COBRA.FR02_DEF_INT_BW_CONV 
WHERE  BALANC_TYPE_CD NOT IN (SELECT BALANC_TYPE_CD FROM CC_COBRA.FR17_BALANC_TYPE_CD_LOOKUP WHERE IBB_FLAG = 'N')
AND    PERIOD_END_DT IN (SELECT REPORT_DT FROM CC_COBRA.WK_FR02_REPORT_DATE_R2)
GROUP BY 1,2,3
) A
JOIN
(
SELECT ORG_TX
      ,PERIOD_END_DT
      ,SUM(DEFERRED_INT_AM) AS IBB_DEFERRED_CONV_INT_AM
FROM   CC_COBRA.FR02_DEF_INT_BW_CONV 
WHERE BALANC_TYPE_CD NOT IN (SELECT BALANC_TYPE_CD FROM CC_COBRA.FR17_BALANC_TYPE_CD_LOOKUP WHERE IBB_FLAG = 'N')
AND   NM_BALANC_TYPE_CD NOT IN (SELECT BALANC_TYPE_CD FROM CC_COBRA.FR17_BALANC_TYPE_CD_LOOKUP WHERE NM_IBB_FLAG = 'N')
GROUP BY 1,2
) B
ON A.ORG_TX = B.ORG_TX
AND A.PERIOD_END_DT = B.PERIOD_END_DT
JOIN
(
SELECT ORG_TX
      ,PERIOD_END_DT
      ,SUM(DEFERRED_INT_AM) AS NIBB_DEFERRED_INT_AM
FROM   CC_COBRA.FR02_DEF_INT_BW_CONV 
WHERE BALANC_TYPE_CD IN ('RNIBB')
GROUP BY 1,2
) C
ON A.ORG_TX = C.ORG_TX
AND A.PERIOD_END_DT = C.PERIOD_END_DT
JOIN
(
SELECT ORG_TX
      ,PERIOD_END_DT
      ,SUM(DEFERRED_INT_AM) AS NIBB_DEFERRED_CONV_INT_AM
FROM   CC_COBRA.FR02_DEF_INT_BW_CONV 
WHERE BALANC_TYPE_CD IN ('RNIBB')
AND   NM_BALANC_TYPE_CD NOT IN (SELECT BALANC_TYPE_CD FROM CC_COBRA.FR17_BALANC_TYPE_CD_LOOKUP WHERE NM_IBB_FLAG = 'N')
GROUP BY 1,2
) D
ON A.ORG_TX = D.ORG_TX

AND A.PERIOD_END_DT = D.PERIOD_END_DT;

.IF ERRORCODE <> 0 THEN .GOTO FOUNDERROR;

COLLECT STATS ON CC_COBRA.FR02_DEF_INT_BW_CONV_REPORT INDEX ( ORG_TX ,PERIOD_END_DT,REC_TYPE );

